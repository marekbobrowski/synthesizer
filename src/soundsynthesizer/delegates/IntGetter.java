package soundsynthesizer.delegates;

/**
 * This interface works as a delegate class for a getter of int type.
 *
 * @author Marek Bobrowski
 */
public interface IntGetter {

    /**
     * This method works as a delegate for a getter of int type.
     *
     * @return the returned value of the assigned getter.
     */
    int get();
}
