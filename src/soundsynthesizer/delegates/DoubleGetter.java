package soundsynthesizer.delegates;

/**
 * This interface works as a delegate class for a getter of double type.
 *
 * @author Marek Bobrowski
 */
public interface DoubleGetter {

    /**
     * This method works as a delegate for a getter of double type.
     *
     * @return the returned value of the assigned getter.
     */
    double get();
}

